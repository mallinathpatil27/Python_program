#display week day name by taking day number

day=int(input('enter day no(1-7):'))

if day==1:
    print('sunday')
elif day==2:
    print('monday')
elif day==3:
    print('tuesday')
elif day==4:
    print('wed')
elif day==5:
    print('thursday')
elif day==6:
    print('friday')
elif day==7:
    print('saturday')
else:
    print('please enter correct day')
    
    
